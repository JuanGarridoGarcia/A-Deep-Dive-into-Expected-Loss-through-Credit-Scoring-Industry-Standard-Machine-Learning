def _normalize_titles(series: pd.Series) -> pd.Series:
    """
    Lowercase, strip whitespace and remove non-alphabetic characters.
    Collapses obvious variants like 'Teacher' / 'TEACHER' / 'teacher'.
    """
    return (
        series
        .str.lower()
        .str.strip()
        .str.replace(r"[^a-z\s]", "", regex=True)
        .str.strip()
    )


def _frequency_filter(series: pd.Series, min_freq: int) -> pd.Series:
    """
    Replace categories appearing fewer than min_freq times with 'Other'.
    Preserves NaNs.
    """
    freq = series.value_counts()
    rare = freq[freq < min_freq].index
    return series.apply(lambda x: "Other" if x in rare else x)


def _semantic_mapping(title: str) -> str:
    """
    Maps a normalized job title to a high-level professional category
    using keyword matching against KEYWORD_MAPPING.
    The first matching rule wins, so order in KEYWORD_MAPPING matters.
    Titles with no matching keyword are classified as 'Other'.
    NaNs are classified as 'Unknown'.
    """
    if pd.isna(title) or title == "":
        return "Unknown"
    t = title.lower().strip()
    for keywords, category in KEYWORD_MAPPING:
        if any(kw in t for kw in keywords):
            return category
    return "Other"


