def group_emp_title(
    df: pd.DataFrame,
    col: str = "emp_title",
    min_freq: int = 50,
    output_col: str = "emp_title_grouped",
) -> pd.DataFrame:
    """
    Full pipeline to group emp_title into high-level professional categories.

    Steps
    -----
    1. Normalize raw text (lowercase, strip, remove special characters).
    2. Frequency filter: titles appearing fewer than min_freq times → 'Other'.
    3. Keyword-based semantic mapping using KEYWORD_MAPPING.
    4. NaN imputation with 'Unknown'.

    Parameters
    ----------
    df         : DataFrame containing the raw emp_title column.
    col        : Name of the column to process (default 'emp_title').
    min_freq   : Titles appearing fewer than this number of times are
                 collapsed into 'Other' before mapping (default 50).
    output_col : Name of the new grouped column (default 'emp_title_grouped').

    Returns
    -------
    df with a new column `output_col`. The original column is preserved.
    """

    print(f"→ Step 1: Normalizing text in '{col}'...")
    normalized = _normalize_titles(df[col])
    print(f"  Unique titles before normalization : {df[col].nunique():,}")
    print(f"  Unique titles after normalization  : {normalized.nunique():,}")

    print(f"\n→ Step 2: Frequency filter (min_freq={min_freq})...")
    filtered = _frequency_filter(normalized, min_freq=min_freq)
    print(f"  Unique titles after frequency filter: {filtered.nunique():,}")

    print(f"\n→ Step 3: Applying keyword-based semantic mapping...")
    df[output_col] = filtered.apply(_semantic_mapping)

    # ── Summary ──────────────────────────────────────────────────────────
    print(f"\n✓ Done. Distribution of '{output_col}':")
    dist = (
        df[output_col]
        .value_counts()
        .rename_axis("category")
        .reset_index(name="n")
    )
    dist["pct"] = (dist["n"] / len(df) * 100).round(2)
    print(dist.to_string(index=False))

    return df

